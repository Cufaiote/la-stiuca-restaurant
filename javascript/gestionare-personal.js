function afiseazaFormularAngajare()
{
  document.getElementById("formularAngajare").style.height     = "auto";
  document.getElementById("formularAngajare").style.visibility = "visible";

}

function anuleazaAngajare() {
  document.getElementById("formularAngajare").style.height     = "0px";
  document.getElementById("formularAngajare").style.visibility = "hidden";

}
